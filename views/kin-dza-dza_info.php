<h2>Личный отзыв</h2>
<p>Один из немногих фантастических фильмов в СССР, расчитанный на взрослую аудиторию. Может показаться, что фильм относительно известен, но по наблюдениям многие его не видели. Некоторых отпугивает малый бюджет картины, по сравнению персонажи(Юрий Яковлев и Евгений Леонов играют совсем нетипичные для себя роли) и сама история с интересными идеями и посылами.
</p>