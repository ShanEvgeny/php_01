<h2>Сюжет</h2>
<p>Прораб Владимир Николаевич Машков и не подозревал, что обычный путь до универсама за хлебом и макаронами обернется межгалактическими путешествиями. А все эта встреча со странным босоногим человеком с каким-то маленьким устройством — «машинкой перемещения», как тот ее назвал. Владимир Николаевич нажал кнопку и оказался вместе со случайным попутчиком, студентом в кроличьей шапке, в пустыне — на планете Плюк в тентуре, галактика Кин-дза-дза в спирали.
</p>
<p>Жанры: драма, фантастика, комедия</p> 
